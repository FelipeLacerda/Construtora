    private void jbsairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbsairActionPerformed
        int resp1 = JOptionPane.showConfirmDialog(null, MensagemUtilUsuario.getValor(MensagemUtilUsuario.MSG_SAIR), MensagemUtilUsuario.getValor(MensagemUtilUsuario.CABE_SAIR), JOptionPane.YES_NO_OPTION);
        if (resp1 == 0) {
            int resp2 = JOptionPane.showConfirmDialog(null, MensagemUtilUsuario.getValor(MensagemUtilUsuario.MSG_LOGIN), MensagemUtilUsuario.getValor(MensagemUtilUsuario.CABE_LOGIN), JOptionPane.YES_NO_OPTION);
            if (resp2 == 0) {
                dispose();
                Login login = new Login(new javax.swing.JFrame(), true);
                login.setVisible(true);
            } else {
                dispose();
            }
        }
    }//GEN-LAST:event_jbsairActionPerformed

