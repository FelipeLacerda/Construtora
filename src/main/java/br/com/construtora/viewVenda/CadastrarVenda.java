    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        contrato.setCertificacaoCli((String) cbcertcli.getSelectedItem());
        contrato.setCertificacaoAdm((String) cbcertadm.getSelectedItem());
        contrato.setCertificacaoImo((String) cbcertimo.getSelectedItem());

        try {
            int resp = JOptionPane.showConfirmDialog(null, MensagemUtilUsuario.getValor(MensagemUtilUsuario.MSG_CAD), MensagemUtilUsuario.getValor(MensagemUtilUsuario.CABE_CAD), JOptionPane.YES_NO_OPTION);
            if (resp == 0) {
                contControl.salvarContrato(contrato);
                JOptionPane.showMessageDialog(null, MensagemUtilContrato.getValor(MensagemUtilContrato.MSG_SUCESSO_SALVARCONT));
                dispose();
            }
        } catch (Exception ex) {
            Logger.getLogger(CadastrarVenda.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, MensagemUtilContrato.getValor(MensagemUtilContrato.MSG_ERRO_SALVARCONT), MensagemUtilUsuario.getValor(MensagemUtilUsuario.CABE_ERRO), JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

