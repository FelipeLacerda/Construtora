    private void jbproimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbproimoActionPerformed
        String senha = (String) tfsenhaimo.getText();
        try {
            imovel = imoControl.procurarImovelSenha(senha);
        } catch (Exception ex) {
            Logger.getLogger(CadastrarVenda.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, MensagemUtilImovel.getValor(MensagemUtilImovel.MSG_ERRO_PROCURARIMO), MensagemUtilUsuario.getValor(MensagemUtilUsuario.CABE_ERRO), JOptionPane.ERROR_MESSAGE);
        }
        if (imovel.getIdImovel().equals(senha)) {
            String[] colunas = {"ID Imóvel: ", "Tamanho: ", "Preço: ", "Andamento: ", "Cidade: "};
            Object[][] dados = {
                {imovel.getIdImovel(), imovel.getTamanho(), imovel.getPreco(), imovel.getAndamentoObra(), imovel.getEnderecoImovel().getCidade()}
            };
            tbimovel.setModel(new DefaultTableModel(dados, colunas));

        } else {
            JOptionPane.showMessageDialog(null, MensagemUtilImovel.getValor(MensagemUtilImovel.MSG_ERRO_PROCURARIMO), MensagemUtilUsuario.getValor(MensagemUtilUsuario.CABE_ERRO), JOptionPane.ERROR_MESSAGE);
        }
        contrato.setImovel(imovel);
    }//GEN-LAST:event_jbproimoActionPerformed

    private void jbprocliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbprocliActionPerformed
        String senha = (String) tfsenhacli.getText();
        try {
            cliente = cliControl.procurarCliente(senha);
        } catch (Exception ex) {
            Logger.getLogger(CadastrarVenda.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, MensagemUtilCliente.getValor(MensagemUtilCliente.MSG_ERRO_PROCURARCLI), MensagemUtilUsuario.getValor(MensagemUtilUsuario.CABE_ERRO), JOptionPane.ERROR_MESSAGE);
        }
        if (cliente.getUsuarioCli().getSenha().equals(senha)) {
            String[] colunas = {"ID Cliente: ", "Nome: ", "CPF: "};
            Object[][] dados = {
                {cliente.getIdCli(), cliente.getNomeCli(), cliente.getCpfCli()}
            };
            tbcli.setModel(new DefaultTableModel(dados, colunas));

        } else {
            JOptionPane.showMessageDialog(null, MensagemUtilCliente.getValor(MensagemUtilCliente.MSG_ERRO_PROCURARCLI), MensagemUtilUsuario.getValor(MensagemUtilUsuario.CABE_ERRO), JOptionPane.ERROR_MESSAGE);
        }
        contrato.setCliente(cliente);
    }//GEN-LAST:event_jbprocliActionPerformed

