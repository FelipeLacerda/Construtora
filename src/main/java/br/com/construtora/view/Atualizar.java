                tfantcidade.setText(adm.getEnderecoAdm().getCidade());
                tfrua.setText(adm.getEnderecoAdm().getRua());
                tfbairro.setText(adm.getEnderecoAdm().getBairro());
                tfuf.setText(adm.getEnderecoAdm().getUf());
                tfnumero.setText(adm.getEnderecoAdm().getNumero());
