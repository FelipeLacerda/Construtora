    private void jbatualizardadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbatualizardadosActionPerformed
        Usuario usu = new Usuario();
        Endereco ende = new Endereco();
        Contato cont = new Contato();
        Administrador admin = new Administrador();

        String resp = (String) cbselecione.getSelectedItem();

        if (resp.equals("Cliente")) {
            
            Long idCli = Long.valueOf(jlid.getText());
            cliente.setIdCli(idCli);

            usu.setLogin(tflogin.getText());
            usu.setSenha(tfsenha.getText());
            cliente.setUsuarioCli(usu);

            cliente.setNomeCli(tfnome.getText());
            cliente.setCpfCli(tfcpf.getText());

            DateUtil datautil = new DateUtil();
            Date data = datautil.stringToDate(tfdata.getText());
            cliente.setDataNascimento(data);

            Long idEnde = Long.valueOf(jlidende.getText());
            ende.setId(idEnde);
            ende.setCep(tfcep.getText());
            ende.setUf(tfuf.getText());
            ende.setNumero(tfnumero.getText());
            if (!cbcidade.getSelectedItem().equals("SELECIONE")) {
                ende.setCidade((String) cbcidade.getSelectedItem());
            } else {
                ende.setCidade(tfantcidade.getText());
            }
            ende.setRua(tfrua.getText());
            ende.setBairro(tfbairro.getText());
            ende.setComplemento(tacomplemento.getText());
            cliente.setEnderecoCli(ende);

            Long idCont = Long.valueOf(jlidcont.getText());
            cont.setId(idCont);
            cont.setCelular(tfcelular.getText());
            cont.setEmail(tfemail.getText());
            cliente.setContatoCli(cont);

            try {
                clienteControl.atualizarCliente(cliente);
                JOptionPane.showMessageDialog(null,MensagemUtilCliente.getValor(MensagemUtilCliente.MSG_SUCESSO_EDITARCLI));
                dispose();
            } catch (Exception ex) {
                Logger.getLogger(Atualizar.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (resp.equals("Administrador")) {
            
             Long idAdm = Long.valueOf(jlid.getText());
            admin.setIdAdm(idAdm);

            usu.setLogin(tflogin.getText());
            usu.setSenha(tfsenha.getText());
            admin.setUsuarioAdm(usu);

            admin.setNomeAdm(tfnome.getText());
            admin.setCpfAdm(tfcpf.getText());

            /*DateUtil datautil = new DateUtil();
            Date data = datautil.stringToDate(tfdata.getText());
            admin.setDataNascimento(data);*/

            Long idEnde = Long.valueOf(jlidende.getText());
            ende.setId(idEnde);
            ende.setCep(tfcep.getText());
            ende.setUf(tfuf.getText());
            ende.setNumero(tfnumero.getText());
            if (!cbcidade.getSelectedItem().equals("SELECIONE")) {
                ende.setCidade((String) cbcidade.getSelectedItem());
            } else {
                ende.setCidade(tfantcidade.getText());
            }
            ende.setRua(tfrua.getText());
            ende.setBairro(tfbairro.getText());
            ende.setComplemento(tacomplemento.getText());
            admin.setEnderecoAdm(ende);

            Long idCont = Long.valueOf(jlidcont.getText());
            cont.setId(idCont);
            cont.setCelular(tfcelular.getText());
            cont.setEmail(tfemail.getText());
            admin.setContatoAdm(cont);

            try {
                admControl.atualizarAdm(admin);
                JOptionPane.showMessageDialog(null,MensagemUtilAdm.getValor(MensagemUtilAdm.MSG_SUCESSO_EDITARADM));
                dispose();
            } catch (Exception ex) {
                Logger.getLogger(Atualizar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_jbatualizardadosActionPerformed

